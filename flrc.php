	Family Livelihood Resource Center
</div>

<div id="rule">
<hr />
</div>

<div id="body">
<p>It is now well known that the poor households derive their livelihoods from a number of different activities, spanning different sectors and sub-sectors. At the same time, the portfolio of livelihood activities performed by a household is unique, and there is a need to bring about an overall increase in the household income, rather than altering the proportions contributed by different activities. Supporting and promoting livelihoods of the poorest and the most vulnerable section of the society, thus, requires an integrated approach that also concentrates on the household as a unit. This realisation has led to the conceptualisation of Family Livelihood Resource Centre (FLRC) approach for the customised diagnosis, design, and delivery of livelihood support inputs required by each family in an area, to come out of the poverty trap on a permanent basis. These inputs can be in the form of information, technical, institutional, or business development support provided to increase the household's access to livelihood assets/resources or to opportunities for income generation, or for vulnerability and risk reduction.
</p>
<p>The main objective of DVS in establishing FLRCs is to enable aggregation of support inputs pertaining to various sectors and sub-sectors, and their delivery as part of a more focused livelihood intervention with the identified, poorest of the poor families in an area. </p>
<p>The objective of the FLRC will be to develop and deliver a customised package of livelihood resources and support services, for sustainable income generation and enterprise promotion, of the poorest and the most vulnerable households in its area.</p>
